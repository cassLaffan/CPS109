# Now let's do the classic opening program whenever we learn a new language.
# Let's tell the computer to print "Hello, world!" to our terminal.
# Remember: the terminal is that funky looking window/blinking cursor where your text
# is fed out.

print("Hello, world!\n")

# Notice the '\n' at the end of the line? That's how the computer knows to go to the
# next line of text. That way, you don't have everything printing off on one line!
# It is known as the "newline character".

# However, when you run this program, you'll notice that it will skip a line, 
# leaving a blank line after our "Hello, world!" output.
# That's because the print function in Python 3 automatically puts a newline character at the end of strings.