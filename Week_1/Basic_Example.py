# Our very first python example!
# Adding the pound sign (hashtag) in front of a line comments it out
# That means that when the machine reads your code, it will ignore everything
# on the line after the hashtag.

# To "run" this program (in a text editor such as VSCode or in your terminal/command line)
# type in "Example_One.py" and hit Enter. It won't read out any of the text printed out.

print("This is how you print a line")

# Notice how after the word print the line we actually want to print is in brackets?
# What is in those brackets is called an "Argument".
# An argument is what you put in a function to make it do what you like.
# In Python 3 (the version we're working in), print is a function.