import math

# This file will just show a some of the neat things you can call
# in the math module.

if __name__ == "__main__":
    # Some constants!
    circumference = 2 * math.pi * 5
    print(circumference)

    circumference = math.tau * 5
    print(circumference)

    # You can call Euler's Number
    print(math.e)

    # And infinity (but it's only useful when doing algorithms)
    print(math.inf)

